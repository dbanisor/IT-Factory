'''
Design patterns (sabloane de proiectare) sunt solutii tipice la probleme comune in proiectarea software.
Fiecare sablon este ca un model care poate fi personalizat pentru a rezolva o anumita problema de design din codul vostru.
Ele definesc un limbaj comun care ajuta echipa de dezvoltare sa comunice mai eficient.
Sabloanele difera prin complexitatea lor, nivelul de detaliu si scara de aplicabilitate la intregul sistem proiectat.
'''

'''
Tipuri de sabloane:
    1. Creational (sabloane de creatie) --> ofera mecanisme de creare a obiectelor care maresc flexibilitatea si reutilizarea codului existent.
    2. Structural (sabloane de structura) --> explica cum sa fie asamblate obiectele si clasele in structuri mai mari, pastrand in acelasi timp flexibilitatea si eficienta lor.
    3. Behavioral (sabloane comportamentale) --> se ocupa de comunicarea eficienta a obiectelor si de repartizarea responsabilitatilor intre acestea. 
'''